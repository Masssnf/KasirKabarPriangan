    public void tampilkandata() {
        DefaultTableModel tabelharga = new DefaultTableModel();
        tabelharga.addColumn("LANGGANAN");
        tabelharga.addColumn("ECERAN");
    
        try {
            String sql = "select * from harga";
            // Pastikan nama class koneksi benar (Koneksi atau KoneksiDatabase)
            java.sql.Connection conn = Koneksi.getConnection(); 
            java.sql.Statement stat = conn.createStatement();
            java.sql.ResultSet res = stat.executeQuery(sql);
        
            while (res.next()) {
                // 1. Ambil data mentah (misal: "100000")
                String hargaL = res.getString("langganan");
                String hargaE = res.getString("eceran");
            
                // 2. Format jadi Rupiah (misal: "Rp 100.000")
                // Pastikan fungsi helper 'formatRupiah' sudah ada di bawah
                String formatL = formatRupiah(hargaL);
                String formatE = formatRupiah(hargaE);
            
                // 3. Masukkan ke tabel
                // PERBAIKAN: Gunakan 'tabelharga', bukan 'model'
                // PERBAIKAN: Syntax array yang benar adalah new Object[] {...}
                tabelharga.addRow(new Object[] { formatL, formatE });
            }
        
            // Pasang model ke JTable di layar
            tblHarga.setModel(tabelharga);
        
            } catch (Exception e) {
            // Tampilkan error agar tahu jika ada masalah
            javax.swing.JOptionPane.showMessageDialog(this, "Error Tampil Data: " + e.getMessage());
        }
    }
    
    private String formatRupiah(String angka) {
        if (angka == null || angka.isEmpty()) {
            return "";
        }
        
        String bersih = angka.replaceAll("[^\\d]", "");
        
        if (bersih.isEmpty()) {
            return "";
        }
        
        try {
            long value = Long.parseLong(bersih);
            java.text.DecimalFormat kursIndo = (java.text.DecimalFormat) java.text.DecimalFormat.getCurrencyInstance();
            java.text.DecimalFormatSymbols formatRP  = new java.text.DecimalFormatSymbols();
            
            formatRP.setCurrencySymbol("Rp ");
            formatRP.setMonetaryDecimalSeparator(',');
            formatRP.setGroupingSeparator('.');
            
            kursIndo.setDecimalFormatSymbols(formatRP);
            kursIndo.setMaximumFractionDigits(0);
            
            return kursIndo.format(value);
            
        } catch (NumberFormatException e) {
            return "";
        }
    }

// 2. Fungsi untuk membersihkan format saat mau disimpan (Rp 10.000 -> 10000)
private String bersihkanRupiah(String angka) {
    if (angka == null || angka.isEmpty()) return "0";
    return angka.replaceAll("[^0-9]", "");
}
