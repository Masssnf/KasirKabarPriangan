    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String langganan = txtLangganan.getText();
        String eceran = txtEceran.getText();

        if (langganan.isEmpty()||eceran.isEmpty()){
            JOptionPane.showMessageDialog(this,"Mohon Isi Seluruh Fieldnya", "HARAP ISI !!!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            String sql = "INSERT INTO harga (langganan, eceran) VALUES (?,?)";

            Connection conn = Koneksi.getConnection();
            PreparedStatement ps = conn.prepareStatement(sql);
            
            String langgananBersih = bersihkanRupiah(txtLangganan.getText());
            String eceranBersih = bersihkanRupiah(txtEceran.getText());

            ps.setString(1, langgananBersih);
            ps.setString(2, eceranBersih);

            int hasil = ps.executeUpdate();

            if (hasil>0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                tampilkandata();
                txtLangganan.setText("");
                txtEceran.setText("");
            }
        }catch (java.sql.SQLException e){
            JOptionPane.showMessageDialog(this, "Gagal Memasukkan Data Wilayah" + e.getMessage());
        };
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            String sql = "UPDATE harga SET eceran=? WHERE langganan=?";
            Connection conn = Koneksi.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);
            
            String eceranRaw = txtEceran.getText();
            String langgananRaw = txtLangganan.getText();
            
            String eceranBersih = bersihkanRupiah(eceranRaw);
            String langgananBersih = bersihkanRupiah(langgananRaw);
            
            pst.setString(1, eceranBersih);
            pst.setString(2, langgananBersih);
            int updated = pst.executeUpdate();

            if (updated > 0) {
                JOptionPane.showMessageDialog(null, "Data Telah Di Update");
                tampilkandata();
                txtLangganan.setText("");
                txtEceran.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Data Gagal Di Update");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (txtLangganan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih Data Dari Tabel Terlebih Dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int konfirmasi = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus data harga: " + txtLangganan.getText() + " ?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM harga WHERE langganan = ?";
                
                java.sql.Connection conn = Koneksi.getConnection();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                
                String idBersih = bersihkanRupiah(txtLangganan.getText());
                pst.setString(1, idBersih);
                
                int rowsAffected = pst.executeUpdate();
                
                if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                tampilkandata(); // Refresh tabel
                
                // Kosongkan form setelah hapus
                txtLangganan.setText("");
                txtEceran.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal Menghapus (Data tidak ditemukan di database)");
                }            
            }catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

