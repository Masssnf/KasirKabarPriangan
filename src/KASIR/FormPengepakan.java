    public void tampilkandata() {
        DefaultTableModel tabelpengepakan = new DefaultTableModel();
        tabelpengepakan.addColumn("KODE PENGEPAKAN");
        tabelpengepakan.addColumn("PENGEPAKAN");
        
        try {
            String sql = "select * from pengepakan";
            Connection conn = Koneksi.getConnection();
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabelpengepakan.addRow(new Object[]{res.getString(1), res.getString(2)});
            }
            tblPengepakan.setModel(tabelpengepakan);
        } catch (Exception e) {
        }
    }

