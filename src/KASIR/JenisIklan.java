    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String jenis_iklan = txtJenisIklan.getText();
        String harga_iklan = txtHargaIklan.getText();
        String muat = txtMuat.getText();
        String warna_iklan = txtWarnaIklan.getText();
        
        if (jenis_iklan.isEmpty()||harga_iklan.isEmpty()||muat.isEmpty()||warna_iklan.isEmpty()){
            JOptionPane.showMessageDialog(this,"Mohon Isi Seluruh Fieldnya", "HARAP ISI !!!", JOptionPane.WARNING_MESSAGE);
        }
        
        try {
            String sql = "INSERT INTO jnsIklan (jenis_iklan, harga_iklan, muat, warna_iklan) VALUES (?,?,?,?)";
            
            Connection conn = Koneksi.getConnection();
            PreparedStatement ps = conn.prepareStatement(sql);
            
            ps.setString(1, jenis_iklan);
            ps.setString(2, harga_iklan);
            ps.setString(3, muat);
            ps.setString(4, warna_iklan);
             
            int hasil = ps.executeUpdate();
            
            if (hasil>0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                tampilkandata();
                txtJenisIklan.setText("");
                txtHargaIklan.setText("");
                txtMuat.setText("");
                txtWarnaIklan.setText("");
            }
        }catch (java.sql.SQLException e){
            JOptionPane.showMessageDialog(this, "Gagal Memasukkan Data Jenis Iklan" + e.getMessage());
        };
    }//GEN-LAST:event_btnTambahActionPerformed

    private void tblJenisIklanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJenisIklanMouseClicked
        int baris = tblJenisIklan.getSelectedRow();
        
        String jenis_iklan = tblJenisIklan.getValueAt(baris, 0).toString();
        String harga_iklan = tblJenisIklan.getValueAt(baris, 1).toString();
        String muat = tblJenisIklan.getValueAt(baris, 2).toString();
        String warna_iklan = tblJenisIklan.getValueAt(baris, 3).toString();
        
        txtJenisIklan.setText(jenis_iklan);
        txtHargaIklan.setText(harga_iklan);
        txtMuat.setText(muat);
        txtWarnaIklan.setText(harga_iklan);
        
    }//GEN-LAST:event_tblJenisIklanMouseClicked

