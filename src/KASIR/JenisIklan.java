    public void tampilkandata() {
        DefaultTableModel tabeliklan = new DefaultTableModel();
        tabeliklan.addColumn("JENIS IKLAN");
        tabeliklan.addColumn("HARGA IKLAN");
        tabeliklan.addColumn("MUAT");
        tabeliklan.addColumn("WARNA IKLAN");
        
        try {
            String sql = "select * from jnsIklan";
            Connection conn = Koneksi.getConnection();
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabeliklan.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4)});
            }
            tblJenisIklan.setModel(tabeliklan);
        } catch (Exception e) {
        }
    }

