    public void tampilkandata() {
        DefaultTableModel tabelwilayah = new DefaultTableModel();
        tabelwilayah.addColumn("KODE WILAYAH");
        tabelwilayah.addColumn("WILAYAH");
        
        try {
            String sql = "select * from wilayah";
            Connection conn = Koneksi.getConnection();
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabelwilayah.addRow(new Object[]{res.getString(1), res.getString(2)});
            }
            tblWilayah.setModel(tabelwilayah);
        } catch (Exception e) {
        }
    }

