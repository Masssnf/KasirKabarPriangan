    public void tampilkandata() {
        DefaultTableModel tabeldataagen = new DefaultTableModel();
        tabeldataagen.addColumn("KODE");
        tabeldataagen.addColumn("NAMA");
        tabeldataagen.addColumn("ALAMAT");
        tabeldataagen.addColumn("KOTA");
        
        try {
            String sql = "select * from dataagen";
            Connection conn = Koneksi.getConnection();
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabeldataagen.addRow(new Object[]{res.getString(1), res.getString(2),res.getString(3),res.getString(4)});
            }
            tblDataAgen.setModel(tabeldataagen);
        } catch (Exception e) {
        }
    }

