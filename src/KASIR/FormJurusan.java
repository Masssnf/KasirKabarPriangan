    public void tampilkandata() {
        DefaultTableModel tabeljurusan = new DefaultTableModel();
        tabeljurusan.addColumn("KODE JURUSAN");
        tabeljurusan.addColumn("JURUSAN");
        
        try {
            String sql = "select * from jurusan";
            Connection conn = Koneksi.getConnection();
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabeljurusan.addRow(new Object[]{res.getString(1), res.getString(2)});
            }
            tblKodeJurusan.setModel(tabeljurusan);
        } catch (Exception e) {
        }
    }
    
